#  Python program that takes two integer inputs from the user and prints the maximum of the two using max().

a = int(input("Enter the a value : "))
b = int(input("Enter the b value : "))
print("Maximum of a, b is : ", max(a, b))

'''
OUTPUT

Enter the a value : 3
Enter the b value : 7
Maximum of a, b is :  7
'''
